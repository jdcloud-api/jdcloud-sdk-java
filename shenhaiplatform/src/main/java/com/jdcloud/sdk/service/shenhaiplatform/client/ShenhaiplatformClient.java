    public interface Builder {
        public Builder credentialsProvider(CredentialsProvider provider);
        public Builder environment(Environment environment);
        public Builder httpRequestConfig(HttpRequestConfig config);
        public ShenhaiplatformClient build() throws JdcloudSdkException;
    }

    private static class DefaultBuilder implements Builder {

        private ShenhaiplatformClient shenhaiplatformClient;

        public DefaultBuilder() {
            shenhaiplatformClient = new ShenhaiplatformClient();
        }

        public DefaultBuilder credentialsProvider(CredentialsProvider provider) {
            shenhaiplatformClient.credentialsProvider = provider;
            return this;
        }

        public DefaultBuilder httpRequestConfig(HttpRequestConfig config) {
            shenhaiplatformClient.httpRequestConfig = config;
            return this;
        }

        public ShenhaiplatformClient build() throws JdcloudSdkException {

            if (shenhaiplatformClient.credentialsProvider == null) {
                //if credentialsProvider not set, try load jdcloud global default  credentials provider
                shenhaiplatformClient.credentialsProvider = Jdcloud.defaultInstance().getCredentialsProvider();
                if (shenhaiplatformClient.credentialsProvider == null) {
                    throw new JdcloudValidateException("ShenhaiplatformClient build error: jdcloud credentials provider not set");
                }
            }
            if (shenhaiplatformClient.httpRequestConfig == null) {
                shenhaiplatformClient.httpRequestConfig = Jdcloud.defaultInstance().getHttpRequestConfig();
                if (shenhaiplatformClient.httpRequestConfig == null) {
                    throw new JdcloudValidateException("ShenhaiplatformClient build error: http request config not set");
                }
            }
            return shenhaiplatformClient;
        }

        public Builder environment(Environment environment) {
            shenhaiplatformClient.environment = environment;
            return this;
        }
    }
